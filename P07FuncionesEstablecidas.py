numeros = [4, 7, 1, 9, 3, 15, 6, 2]
 
print("Lista:", numeros)
print("Longitud (len):", len(numeros))
print("Máximo (max):", max(numeros))
print("Mínimo (min):", min(numeros))
print("Suma (sum):", sum(numeros))
print("Ordenada (sorted):", sorted(numeros))
print("Valor absoluto de -42 (abs):", abs(-42))
print("Redondeo de 3.7 (round):", round(3.7))
print("Tipo de dato de 'Hola' (type):", type("Hola"))
print("Rango del 1 al 5 (range):", list(range(1, 6)))
print("Convertir '123' a entero (int):", int("123"))
print("Convertir 3.14 a cadena (str):", str(3.14))
